            String email = String.valueOf(tablaListarGanadero.getValueAt(tablaListarGanadero.getSelectedRow(), 6));

            int confirmacion = JOptionPane.showConfirmDialog(
                    this,
                    "¿Deseas activar el registro \"" + nombre + "\"?",
                    "Activar registro",
                    JOptionPane.YES_NO_OPTION
            );

