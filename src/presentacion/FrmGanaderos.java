            String direccion = String.valueOf(tablaListarGanadero.getValueAt(tablaListarGanadero.getSelectedRow(), 5));
            String email = String.valueOf(tablaListarGanadero.getValueAt(tablaListarGanadero.getSelectedRow(), 6));

            // Asigna los valores a los campos del formulario
