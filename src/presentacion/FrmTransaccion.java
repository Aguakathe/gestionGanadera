    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (txtGanaderoid.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Debes ingresar un ID de ganadero, es obligatorio", "Sistema", JOptionPane.WARNING_MESSAGE);
            txtGanaderoid.requestFocus();
            return;
        }

        try {
            // ConversiÃ³n de datos
            int ganaderoId = Integer.parseInt(txtGanaderoid.getText());
            String tipoTransaccion = txtTipoTransaccion.getText();
            String fecha = txtFecha.getText();
            double total = Double.parseDouble(txtTotal.getText());
            String estado = txtEstado.getText();

            // ValidaciÃ³n bÃ¡sica del estado (opcional)
            if (!estado.equalsIgnoreCase("pendiente")
                    && !estado.equalsIgnoreCase("completado")
                    && !estado.equalsIgnoreCase("cancelado")) {
                JOptionPane.showMessageDialog(this, "Estado invÃ¡lido. Usa: pendiente, completado o cancelado", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            String resp;
            if (this.accion.equals("Editar")) {
                // AquÃ­ irÃ­a el cÃ³digo para editar
            } else {
                // Insertar nueva transacciÃ³n
                resp = this.CONTROL.insertar(ganaderoId, tipoTransaccion, fecha, total, estado);
                if (resp.equals("OK")) {
                    this.mensajeOK("TransacciÃ³n registrada correctamente");
                    this.limpiar();
                    this.listar("");
                    tabGeneral.setSelectedIndex(0);
                    tabGeneral.setEnabledAt(0, true);
                    tabGeneral.setEnabledAt(1, false);
                } else {
                    this.mensajeError(resp);
                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor ingresa un nÃºmero vÃ¡lido en el campo Total.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


